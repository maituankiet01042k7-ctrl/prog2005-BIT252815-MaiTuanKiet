numbers = [7, 2, 5, 9, 5, 1]
print("Danh sách ban đầu:", numbers)

numbers.sort()
print("Danh sách sau khi sắp xếp (sort):", numbers)

numbers.reverse()
print("Danh sách sau khi đảo ngược (reverse):", numbers)

so_lan_xuat_hien = numbers.count(5)
print(f"Số lần xuất hiện của số 5 là: {so_lan_xuat_hien}")
